// Criar uma função
// Que soma 2 numeros e retorna esses numero somado
// Deve ser enviado por parametros os 2 números da conta

// numero1: 20
// numero2: 10
// soma: 30

// Função soma
function soma(numero1, numero2) {
    const resultado = numero1 + numero2
    console.log('Número1: ', numero1)
    console.log('Número2: ', numero2)
    console.log('Soma: ', resultado)
}

soma(10, 20)